public class Exampleofdowhileloop {
    public static void main(String[] args) {
        int i=0;
        do {
            System.out.println(i);
            i++;
        } while(i<10);
    }
}
// Note 1. first intialization is must
// when using do - while -execution will happen atleast before going for condition
// we have to increment inside the do only
//condition will be in while signature
